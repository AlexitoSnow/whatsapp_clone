  void _saveDataToContactsSubcollection(
    UserModel senderUserData,
    UserModel? recieverUserData,
    String text,
    DateTime timeSent,
    String recieverUserId,
    bool isGroupChat,
  ) async {
    if (isGroupChat) {
      await firestore.collection('groups').doc(recieverUserId).update({
        'lastMessage': text,
        'timeSent': DateTime.now().millisecondsSinceEpoch,
      });
    } else {
// users -> reciever user id => chats -> current user id -> set data
      var recieverChatContact = ChatContact(
        phone: senderUserData.phoneNumber,
        profilePic: senderUserData.profilePic,
        contactId: senderUserData.uid,
        timeSent: timeSent,
        lastMessage: text,
      );
      await firestore
          .collection('users')
          .doc(recieverUserId)
          .collection('chats')
          .doc(auth.currentUser!.uid)
          .set(
            recieverChatContact.toMap(),
          )
          .then((_) {
        log('Data saved to $recieverUserId chats subcollection',
            name: 'ChatRepository');
      });
      // users -> current user id  => chats -> reciever user id -> set data
      var senderChatContact = ChatContact(
        phone: recieverUserData!.phoneNumber,
        profilePic: recieverUserData.profilePic,
        contactId: recieverUserData.uid,
        timeSent: timeSent,
        lastMessage: text,
      );
      await firestore
          .collection('users')
          .doc(auth.currentUser!.uid)
          .collection('chats')
          .doc(recieverUserId)
          .set(
            senderChatContact.toMap(),
          )
          .then((_) {
        log('Data saved to ${auth.currentUser!.uid} chats subcollection',
            name: 'ChatRepository');
      });
    }
  }

  void _saveMessageToMessageSubcollection({
    required String recieverUserId,
    required String text,
    required DateTime timeSent,
    required String messageId,
    required String username,
    required MessageType messageType,
    //required MessageReply? messageReply,
    required String senderUsername,
    required String? recieverUserName,
    required bool isGroupChat,
  }) async {
    final message = Message(
      senderId: auth.currentUser!.uid,
      recieverid: recieverUserId,
      text: text,
      type: messageType,
      timeSent: timeSent,
      messageId: messageId,
      isSeen: false,
      //repliedMessage: messageReply == null ? '' : messageReply.message,
      /*repliedTo: messageReply == null
          ? ''
          : messageReply.isMe
              ? senderUsername
              : recieverUserName ?? '',
      repliedMessageType:
          messageReply == null ? MessageEnum.text : messageReply.messageEnum,*/
    );
    if (isGroupChat) {
      // groups -> group id -> chat -> message
      await firestore
          .collection('groups')
          .doc(recieverUserId)
          .collection('chats')
          .doc(messageId)
          .set(
            message.toMap(),
          );
    } else {
      // users -> sender id -> reciever id -> messages -> message id -> store message
      await firestore
          .collection('users')
          .doc(auth.currentUser!.uid)
          .collection('chats')
          .doc(recieverUserId)
          .collection('messages')
          .doc(messageId)
          .set(
            message.toMap(),
          )
          .then((_) {
        log('Data saved to ${auth.currentUser!.uid} messages subcollection',
            name: 'ChatRepository');
      });
      // users -> eciever id  -> sender id -> messages -> message id -> store message
      await firestore
          .collection('users')
          .doc(recieverUserId)
          .collection('chats')
          .doc(auth.currentUser!.uid)
          .collection('messages')
          .doc(messageId)
          .set(
            message.toMap(),
          )
          .then((_) {
        log('Data saved to $recieverUserId messages subcollection',
            name: 'ChatRepository');
      });
    }
  }

  Future<String?> sendTextMessage({
    required String text,
    required String recieverUserId,
    required UserModel senderUser,
    //required MessageReply? messageReply,
    required bool isGroupChat,
  }) async {
    try {
      var timeSent = DateTime.now();
      UserModel? recieverUserData;

      if (!isGroupChat) {
        var userDataMap =
            await firestore.collection('users').doc(recieverUserId).get();
        recieverUserData = UserModel.fromMap(userDataMap.data()!);
      }

      var messageId = const Uuid().v1();

      _saveDataToContactsSubcollection(
        senderUser,
        recieverUserData,
        text,
        timeSent,
        recieverUserId,
        isGroupChat,
      );

      _saveMessageToMessageSubcollection(
        recieverUserId: recieverUserId,
        text: text,
        timeSent: timeSent,
        messageType: MessageType.text,
        messageId: messageId,
        username: senderUser.name ?? '',
        //messageReply: messageReply,
        recieverUserName: recieverUserData?.name,
        senderUsername: senderUser.name ?? '',
        isGroupChat: isGroupChat,
      );
    } catch (e) {
      return e.toString();
    }
    return null;
  }

  Future<String?> sendFileMessage({
    required File file,
    required String recieverUserId,
    required UserModel senderUserData,
    required ProviderRef ref,
    required MessageType messageType,
    //required MessageReply? messageReply,
    required bool isGroupChat,
  }) async {
    try {
      var timeSent = DateTime.now();
      var messageId = const Uuid().v1();

      var imageUrl =
          await ref.read(commonStorageRepositoryProvider).storeFileToFirebase(
                'chat/${messageType.name}/${senderUserData.uid}/$recieverUserId/$messageId',
                file,
              );

      UserModel? recieverUserData;
      if (!isGroupChat) {
        var userDataMap =
            await firestore.collection('users').doc(recieverUserId).get();
        recieverUserData = UserModel.fromMap(userDataMap.data()!);
      }

      String contactMsg = messageType.formatted;
      _saveDataToContactsSubcollection(
        senderUserData,
        recieverUserData,
        contactMsg,
        timeSent,
        recieverUserId,
        isGroupChat,
      );

      _saveMessageToMessageSubcollection(
        recieverUserId: recieverUserId,
        text: imageUrl ?? '',
        timeSent: timeSent,
        messageId: messageId,
        username: senderUserData.name ?? '',
        messageType: messageType,
        //messageReply: messageReply,
        recieverUserName: recieverUserData?.name,
        senderUsername: senderUserData.name ?? '',
        isGroupChat: isGroupChat,
      );
    } catch (e) {
      return e.toString();
    }
    return null;
  }

